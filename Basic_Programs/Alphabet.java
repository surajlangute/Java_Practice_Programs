package Basic_Programs;

public class Alphabet {

	public static void main(String[] args) {
		// char ch ='C'; 
				String ch ="A";

		             /*Only String, Char & Integer can be used in Switch Case
					for String put value in "" & for Char use '' */

			switch (ch)
		     		{
				case "A": System.out.println("A");
					break;

				case "B": System.out.println("B");
					break;

				case "C": System.out.println("C");
					break;

				default : System.out.println("Not in  A , B or C");
					break;

				}
			}
		
	}


